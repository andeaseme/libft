int		ft_atoi(const char *str)
{
	int		i;
	int		sign;
	int		result;

	i = 0;
	result = 0;
	while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	sign = (str[i] == '-') ? -1 : 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] <= '9' && str[i] >= '0')
	{
		result = (10 * result) + str[i] - '0';
		i++;
	}
	return (sign * result);
}
