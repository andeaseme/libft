int		ft_strcmp(const void *s1, const void *s2)
{
	char	*t1;
	char	*t2;

	t1 = (char *)s1;
	t2 = (char *)s2;
	while (*t1 == *t2 && (*t1 != '\0' || *t2 != '\0'))
	{
		t1++;
		t2++;
	}
	return ((unsigned char)*t1 - (unsigned char)*t2);
}
