char	*ft_strrchr(const char *s, int c)
{
	const char	*d;

	d = 0;
	while (*s != '\0')
	{
		if (*s == c)
			d = s;
		s++;
	}
	if (*s == c)
		d = s;
	return ((char *)d);
}
